#!/bin/python
import math

def add (a, b):
    return a+b

def sub (a, b):
    return a-b;

def mult (a, b):
    return a*b 

def div (a, b):
    return a/b

def percent(a, b):
    return (a/b)*100

def sqroot(a):
    return math.sqrt(a)

def sine(a):
    return math.sin(a)

def cosine(a):
    return math.cos(a)

def squared(a):
    return a**2

def logarithm(a):
    return math.log(a)

